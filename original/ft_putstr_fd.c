#include "../libft.h"

void	ft_putstr_fd(char *s, int fd)
{
	int	i;

	if (s == NULL)
		return ;
	i = 1;
	if (s[0] != 0)
	{
		while (s[i] != 0)
			i++;
		write(fd, s, i);
	}
}
